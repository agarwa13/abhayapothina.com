            <div class="col-md-6">
                    <p><strong>Design</strong><br>While the business requirements were being fleshed we used the initial few days to
                    brainstorm ideas based on the current ticket sales flow and existing research about ticket purchase behavior of guests at the park. We wireframed multiple concepts, each one reflecting a slightly different guest mental model.
                    Using Axure and Illustrator I built 3 hi-fidelity prototypes of the wireframed concepts. I utilized and contributed to the our design library of axure components while
                    in the process of building the prototypes. We used the prototypes to convey the concepts to the various business stakeholders.
                    While incorporating the business requirements feedback into the designs I also started tightening up the prototype
                    for usability testing.</p>
                <p><strong>Usability Testing</strong><br>We tested the 3 concepts in both Disneyworld and Disneyland over the span of 2 weeks.
                During the testing one of the concepts started emerging as a popular guest mental model.
                Using the feedback we got during the usability testing, we made some updates to the prototype and dived
                into the visual design phase. After going through a few cycle of iterations we landed on a final design and moved to review phase where
                the design was reviewed again with all the business stakeholders and the leadership.<br><br>
                For the tech hand off, I updated the prototype to the latest designs and refined the animations
                to deliver to the development team. Along with the prototype I created a user flow document to show happy paths and edge cases. </p>
